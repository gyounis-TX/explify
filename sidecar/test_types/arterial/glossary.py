"""
Lower Extremity Arterial Doppler glossary:
medical term -> plain English explanation.
"""

ARTERIAL_GLOSSARY: dict[str, str] = {
    "Lower Extremity Arterial Ultrasound": (
        "An ultrasound test that checks the blood flow in the arteries of your "
        "legs. It helps detect blockages that could cause leg pain or poor circulation."
    ),
    "Claudication": (
        "Pain, cramping, or tiredness in the legs during walking that goes away "
        "with rest. It is caused by reduced blood flow to the leg muscles."
    ),
    "Peripheral Arterial Disease (PAD)": (
        "A condition where the arteries in the legs become narrowed or blocked, "
        "reducing blood flow to the feet and legs."
    ),
    "Common Femoral Artery (CFA)": (
        "The main artery in the upper thigh that supplies blood to the leg. "
        "It is one of the first arteries checked in a leg arterial ultrasound."
    ),
    "Profunda Femoris Artery (PFA)": (
        "A deep artery in the thigh that supplies blood to the thigh muscles. "
        "Also called the deep femoral artery."
    ),
    "Superficial Femoral Artery (SFA)": (
        "The artery that runs along the inner thigh, carrying blood from the "
        "groin down toward the knee."
    ),
    "Popliteal Artery (Pop A)": (
        "The artery behind the knee. It is a continuation of the femoral artery "
        "and supplies blood to the lower leg."
    ),
    "Posterior Tibial Artery (PTA)": (
        "An artery in the lower leg that runs behind the shin bone and supplies "
        "blood to the foot. Its pulse can be felt near the inner ankle."
    ),
    "Dorsalis Pedis Artery (DPA)": (
        "An artery on top of the foot. Its pulse can be felt between the first "
        "and second toe tendons."
    ),
    "Anterior Tibial Artery (ATA)": (
        "An artery in the front of the lower leg that becomes the dorsalis pedis "
        "artery at the ankle."
    ),
    "Peroneal Artery": (
        "An artery in the outer part of the lower leg that supplies blood to "
        "the muscles and skin on the side of the leg."
    ),
    "Triphasic Waveform": (
        "A normal blood flow pattern with three phases in each heartbeat. "
        "This indicates healthy arteries without significant blockage."
    ),
    "Biphasic Waveform": (
        "A blood flow pattern with two phases. It can be normal in some people "
        "but may also suggest mild narrowing of the artery."
    ),
    "Monophasic Waveform": (
        "A blood flow pattern with only one phase. This usually indicates "
        "significant narrowing or blockage upstream in the artery."
    ),
    "Patent": (
        "Open and unblocked. A patent artery means blood can flow through "
        "it normally."
    ),
    "Ankle-Brachial Index (ABI)": (
        "A ratio comparing blood pressure at the ankle to blood pressure in the arm. "
        "A normal ABI is 1.0 to 1.4. Values below 0.9 suggest reduced blood flow "
        "to the legs."
    ),
    "Hemodynamically Significant": (
        "Narrowing that is severe enough to affect blood flow. In leg arteries, "
        "this usually means a blockage of 50% or more."
    ),
    "Stenosis": (
        "Narrowing of a blood vessel, usually caused by plaque buildup. "
        "The percentage tells how much the artery is blocked."
    ),
}
